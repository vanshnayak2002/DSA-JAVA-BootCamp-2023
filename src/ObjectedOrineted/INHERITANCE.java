package ObjectedOrineted;

public class INHERITANCE {
    double l;
    double h;
    double w;

    public INHERITANCE(){
        this.l=-1;
        this.h=-2;
        this.w=-1;
    }
 public INHERITANCE(int side){
        this.l=side;
        this.h=side;
        this.w=side;
 }
 public  INHERITANCE(double l,double h,double w){
         this.l=l;
         this.w=w;
         this.h=h;
 }

}
